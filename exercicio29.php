<?php
/******************************************************************************
Descritivo: Dado um array de números [12, 5, 23, 18, 9, 45], imprima apenas os números maiores que 20.
*******************************************************************************/
$numeros = [12, 5, 23, 18, 9, 45];
foreach($numeros as $n){
    if($n > 20){
        echo $n . "<br>";
    }
}
?>